public class SS {

	@Keyword
	public void takeEntirePage(WebDriver webDriver, Integer timeout = 300) {
		WebDriver driver = DriverFactory.getWebDriver()
		Path projectDir = Paths.get(RunConfiguration.getProjectDir())
		Path reportDir = projectDir.resolve('Screenshots')
		Files.createDirectories(reportDir)

		def date = new Date()
		String strDate = date.format('dd_mm_YYYY_hhmmss')
		Path pngFile = reportDir.resolve('screenshot'+strDate+'.png')

		Screenshot screenshot = new AShot().
				coordsProvider(new WebDriverCoordsProvider()).
				shootingStrategy(ShootingStrategies.viewportPasting(timeout)).
				takeScreenshot(webDriver)
		ImageIO.write(screenshot.getImage(), "PNG", pngFile.toFile())
	}
}
